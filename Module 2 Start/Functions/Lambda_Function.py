
# Lambda Function  -  Anonymous function. (' Important Data Science ')

# lambda arg1,arg2,arg3,,.....:expression   -   (Syndax)

# Find square of a number using Lambda fun().
sq=lambda n:n*n
print(sq(5))
print(sq(9))

# Find sum of 3 numbers using Lambda fun().
sum=lambda a,b,c:a+b+c
print(sum(5,10,5))
print(sum(9,8,10))

